#include <stdio.h>

char* decode(char* str)
{
    for(int i = 0; i < 26; i++)
    {
        str[i] -= i;
    }
    return str;
}

int main()
{
    char cypher[27] =  {0x66, 0x34, 0x6b, 0x6d, 0x6d, 0x36, 0x70, 0x7c,  0x3d, 0x82, 0x7f, 0x70, 0x82,0x6e, 0x83, 0x82,\
                        0x44, 0x42, 0x83, 0x44, 0x75, 0x7b, 0x7f, 0x8c,  0x89, 0x0a, 0x00};

    
    char* plain = decode(cypher);
    
    printf("%s", plain);

    return 0;
}